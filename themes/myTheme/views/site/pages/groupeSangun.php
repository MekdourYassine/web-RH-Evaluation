    <p>Lors d’une transfusion, il est important que le groupe sanguin du donneur  soit compatible avec celui du receveur. Il existe plusieurs systèmes antigéniques qui permettent d’identifier les cellules sanguines. Les plus connus sont les systèmes ABO et Rh qui définissent la compatibilité et l'incompatibilité sanguine entre le donneur et le receveur. 

Le système ABO permet d’attribuer à chaque individu une lettre qui caractérise son groupe sanguin : A, B, AB ou O. Le système Rh détermine si vous êtes Rh positif ou négatif.

Dans la population caucasienne (individus présentant des traits physiques européens et une peau claire), la répartition des différents types de sang est la suivante :</p>
    <h3>Répartition des types de sang</h3>
    
    <img src="themes/myTheme/images/groupesangun.jpg" width="350px" style="float: right; padding: 10px" alt="les groupes sanguns">
    <p>Les personnes du groupe O, également appelées
« donneurs universels », peuvent donner leur sang à n’importe quel receveur. Le groupe O- est surtout utilisé dans les situations d'urgence.

A l’inverse, les personnes du groupe AB+ sont les 
« receveurs universels », ils peuvent recevoir du sang de tous les groupes sanguins.

Toutefois, dans la majorité des cas, les receveurs sont transfusés avec le sang d'un donneur de leur propre groupe sanguin.

Pour le plasma, les choses sont différentes : ce sont les personnes du groupe AB qui sont « donneurs universels », leur plasma peut être transfusé à tous les patients. A l’inverse, les personnes du groupe O sont les « receveurs universels », ils peuvent recevoir du plasma de tous les groupes sanguins.</p>
    <h3>Compatibilité des groupes sanguins</h3>
    <img src="themes/myTheme/images/groupesangun1.jpg" width="350px" style="float: right; padding: 20px" alt="les groupes sanguns">
    <p>Pour garantir la sécurité du receveur lors d’une transfusion, il est primordial de respecter les règles de compatibilité selon les groupes sanguins, et le type de produit : globules rouges, plaquettes ou plasma. En cas d’incompatibilité, le système immunitaire du receveur va reconnaître la présence de substances 
« les antigènes », qui lui sont étrangères, ce qui peut entraîner le rejet du composant sanguin et une aggravation de l'état du malade.

Quel que soit votre groupe sanguin, votre sang, votre plasma ou vos plaquettes peuvent sauver des vies !</p>