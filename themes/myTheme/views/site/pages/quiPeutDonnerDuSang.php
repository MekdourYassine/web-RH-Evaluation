    <h2>Conditions d'éligibilité</h2>
    <p>
    Il est difficile de répondre de manière exhaustive à la question de l’éligibilité au don. En effet, un entretien médical est réalisé avant chaque don par un professionnel de la santé. C’est sur cette base et uniquement sur celle-ci qu’une décision est prise. </p><p>
De manière simple, il se peut que certains éléments vous empêchent de donner aujourd’hui mais vous le permettent demain et inversement.
</p>
<p>Quelques notions générales permettent néanmoins de vous situer :</p>

<h3>Vous n'avez jamais donné de sang</h3>
<p>Si vous n’avez jamais donné de sang, vous devez avoir entre 18 et 65 ans pour vous rendre la première fois dans l’un de nos centres. Si par contre, vous avez déjà donné du sang, vous pourrez donner jusque 70 ans si votre dernier don remonte à moins de trois ans au moment où vous vous présentez.
</p>
<h3>Conditions de poids et de taille</h3>
<p>Votre poids et votre taille permettront de vérifier si vous êtes dans les conditions pour donner : dans tous les cas, les personnes de moins de 50kg ne pourront pas donner. De même, le médecin veillera à la prise de la tension artérielle.
</p>
